class Owner {
    public name: String;
    public phone_number: String;
    public email: String;
    public password: String;

    constructor(name: String, phone_number: String, email: String, password: String) {
        this.name = name;
        this.phone_number = phone_number;
        this.email = email;
        this.password = password;

    }

}

export default Owner;